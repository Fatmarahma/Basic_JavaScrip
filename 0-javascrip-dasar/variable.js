// VARIABEL : UNTUK MENYIMPAN DATA

/*  cara membuat variabel :
1- menggunakan kata kunci let
2- menggunakan kata kunci const       */

// tidak disarankan make var karena variable bisa terduplikat dan rancu
var hobby = "menulis";
var hobby = "membaca";

// variable yang di assign mengunakan let datanya bisa di ubah/overwrite
let studentName = "Fatma";
studentName = "danu";

// Variable yang di assign menggunakan const datanya tidak bisa di ubah karena bersifat constant
const age = 22;

console.log("Nama Siswa",studentName);
console.log("Umur :"+ age);
console.log("nama:",studentName,"umur:",age)
console.log(hobby);
